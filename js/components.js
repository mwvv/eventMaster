function formatDate(d) {
  if (!d) return '—';
  const [y,m,day] = d.split('-');
  const meses = ['Ene','Feb','Mar','Abr','May','Jun','Jul','Ago','Sep','Oct','Nov','Dic'];
  return `${day} ${meses[parseInt(m)-1]} ${y}`;
}

function showAlert(el, msg, type='danger') {
  el.textContent = msg;
  el.className = `alert alert-${type}`;
}

// ============================================================
//  MODAL
// ============================================================
function showModal(title, body, onConfirm=null, confirmLabel='Guardar') {
  const html = `
  <div class="modal-overlay" id="modalOverlay" onclick="handleOverlayClick(event)">
    <div class="modal">
      <div class="modal-header">
        <span class="modal-title">${title}</span>
        <button class="btn-close" onclick="closeModal()">
          <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><line x1="18" y1="6" x2="6" y2="18"/><line x1="6" y1="6" x2="18" y2="18"/></svg>
        </button>
      </div>
      <div class="modal-body">${body}</div>
      ${onConfirm ? `<div class="modal-footer">
        <button class="btn btn-ghost" onclick="closeModal()">Cancelar</button>
        <button class="btn btn-primary" onclick="onConfirmCb()">${confirmLabel}</button>
      </div>` : ''}
    </div>
  </div>`;
  document.getElementById('modalContainer').innerHTML = html;
  window._modalConfirmCb = onConfirm;
}

function onConfirmCb() { if (window._modalConfirmCb) window._modalConfirmCb(); }
function closeModal() { document.getElementById('modalContainer').innerHTML = ''; }
function handleOverlayClick(e) { if (e.target.id === 'modalOverlay') closeModal(); }

function showConfirm(title, body, onConfirm) {
  showModal(title, `<p style="color:var(--text-muted);font-size:14px">${body}</p>`, onConfirm, 'Confirmar');
}

// ============================================================
//  TOAST
// ============================================================
function showToast(msg, type='success') {
  const el = document.createElement('div');
  el.className = `toast toast-${type}`;
  const icons = { success:'✓', danger:'✕', warning:'⚠' };
  el.innerHTML = `<span style="color:var(--${type==='success'?'success-text':type==='danger'?'danger-text':'warning-text'})">${icons[type]||'ℹ'}</span> ${msg}`;
  document.getElementById('toastContainer').appendChild(el);
  setTimeout(() => { el.style.opacity='0'; el.style.transition='opacity .3s'; setTimeout(()=>el.remove(),300); }, 3500);
}

