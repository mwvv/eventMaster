function renderNotificacionesPage() {
  const notifs = NOTIFICACIONES.filter(n => n.idDestino === STATE.role || n.idDestino === 'all');
  return `
  <div class="page-header">
    <div class="page-header-text"><h1>Notificaciones</h1><p>Centro de alertas del sistema</p></div>
    <button class="btn btn-outline btn-sm" onclick="marcarTodasLeidas()">Marcar todas como leídas</button>
  </div>
  <div class="card">
    ${notifs.length ? notifs.map(n=>`
    <div style="padding:14px 20px;border-bottom:1px solid var(--border);display:flex;align-items:flex-start;gap:12px;${!n.leida?'background:var(--info-bg);':''}">
      <div style="width:8px;height:8px;border-radius:50%;background:${!n.leida?'var(--accent)':'var(--border)'};margin-top:5px;flex-shrink:0"></div>
      <div style="flex:1">
        <div style="font-size:13px;font-weight:${!n.leida?'500':'400'}">${n.mensaje}</div>
        <div style="font-size:11px;color:var(--text-hint);margin-top:3px">${n.fecha} · <span class="badge badge-brand" style="font-size:10px">${n.tipo}</span></div>
      </div>
      ${!n.leida?`<button class="btn btn-ghost btn-sm" onclick="marcarLeida(${n.id})">${iconCheck()}</button>`:''}
    </div>`).join('') : '<div class="empty-state"><h3>Sin notificaciones</h3><p>No hay notificaciones para mostrar.</p></div>'}
  </div>`;
}

function marcarLeida(id) {
  const n = NOTIFICACIONES.find(x=>x.id===id);
  if (n) n.leida = true;
  showPage('notificaciones');
}

function marcarTodasLeidas() {
  NOTIFICACIONES.forEach(n => { if (n.idDestino===STATE.role||n.idDestino==='all') n.leida=true; });
  showPage('notificaciones');
  showToast('Todas las notificaciones marcadas como leídas.', 'success');
}
